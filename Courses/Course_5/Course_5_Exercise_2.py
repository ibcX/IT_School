
# Exercise 2: Pop all elements from the list fruitsList = ["Mango","Apple","Orange","Guava"] using a while  loop

fruits_list = ["Mango", "Apple", "Orange", "Guava"]
print("Lista initiala: ", fruits_list)

while len(fruits_list) > 0:
    fruits_list.pop()

print("Lista goala: ", fruits_list)
